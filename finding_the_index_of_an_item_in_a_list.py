#  Finding the index of an item in a list

list_a = ['foo','bar','baz'] # création de la variable list_a ayant 3 elements dans sa liste
list_a.index('bar') # savoir quel est l'index de l'item "bar" dans la list_a ?

print(list_a.index('bar')) # ici bar est situé à l'index 1 ( donc en deuxieme position)

print(list_a.index('baz')) # ici bar est situé à l'index 1 ( donc en troisième position)
