# Chaining Of Comparison Operators


n = 10
result = 1 < n < 20  # ici est-ce que le nombre est supérieur à 1, ET inférieur à 20?
print(result) # ici la reponse sera exprimée en booléen True or False




result = 1 > n <= 9 # ici on demande n est égal à 1 ou inférieur ou égal à 9?
print(result) # ici la reponse sera exprimée en booléen True or False
