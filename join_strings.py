# join strings
# joindre les elements d'une liste en une string


test = ['I', 'Like', 'Python', 'automation']
print(' '.join(test)) # joindre les éléménents de la liste test avec 1 espace entre chaque élément

print('***'.join(test)) # joindre les éléménents de la liste test avec 3 étoiles entre chaque élément